function init(){
  if(!(isMobile())){// if browser is not mobile, convert venmo links to web style
    for(const link of document.querySelectorAll("a")){
        if(link.href.startsWith("venmo")){
            convertVenmoLink(link)
        }
    }
  }
}

function convertVenmoLink(link){
    params=link.href.split("?")[1].split("&")
    let url="https://venmo.com/"
    for(let x=0;x<params.length;x++){
        if(params[x].startsWith("recipients")){
          url+=params.splice(x, 1)[0].split("=")[1]
          break
        }
    }
    if(params.length>0){url+="?" + params.join("&")}
    link.href=url
    
}

function showQR(){
  
  const url=document.referrer
  fetch(url).then(response => {
    // Check if the response was successful (e.g., HTTP status 200-299)
    if (!response.ok) {
      throw new Error(`HTTP error! status: ${response.status}`);
    }
    // Parse the response body as JSON
    return response.text();
  })
  .then(data => {
    // Work with the parsed data
  tag("heading").innerHTML=data.split('<div class="heading">')[1].split("</div>")[0].trim()  
  })
  .catch(error => {
    // Handle any errors that occurred during the fetch or parsing
    console.error('Error fetching data:', error);
  });  
  new QRCode("qrcode", {text:url,correctLevel : QRCode.CorrectLevel.M});
  tag("url").innerHTML = url


}

function tag(id){
  return document.getElementById(id)
}